// Number, yaitu tipe data berupa bilangan bulat atau bilangan desimal
// keduanya disatukan didalam JS
// default nilai number itu basis 10, dan jg mendukung jenis number yg lain :
// - hexadecimal = 0x
// - binary = 0b
// - octal = 0o
// ketiga jenis diatas didahului dgn angka 0x, 0b, 0o
console.log(200);
console.log(200.20);
console.log(2_000_000);
console.log(0b110011);
console.log(0xFFA2);
console.log(0o2231);

// Boolean, tipe data yang isinya kebenaran true atau false
console.log(true);
console.log(false);

// String, tipe data yang isinya kumpulan karakter
// Concatanation = + (plus)
// Escape karater, karakter khusus yg bisa digunakan didalam kode program
