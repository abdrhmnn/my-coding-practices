# while
a = 1
while a <= 5:
    # print('welcome')
    print('*' * a)
    a += 1

# for
nama = 'abdu'
# for untuk mengulang sebuah benda yang isinya banyak (collection)
for karakterNama in nama:
    print(karakterNama)

# jika ingin mencetak range angka, maka bisa gunakan bantuan function range
satu = int(input('angka satu : '))
dua = int(input('angka dua : '))
result = satu + dua
for i in range(10, 1, result):
    print(i)

