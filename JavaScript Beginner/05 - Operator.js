// Operator aritmatika, penugasan, perbandigan, logika dan string disebut dengan Operator BINARY
// Operator kondisional disebut dengan Operator TERNARY
// Operator typeof disebut dengan operator UNARY


// Operator Ternary adalah operator yang membutuhkan 3 Operand
// Operator Binary adalah operator yang membutuhkan 2 Operand
// Operator Unary adalah operator yang membutuhkan 1 Operand


// OPERATOR ARITMATIKA
// +, -, *, /, %
// Urutan Operasi MATEMATIKA :
// KuKaBaTaKu = Kurung, Kali, Bagi, Tambah, Kurang


// OPERATOR PENUGASAN (Assignment)
//    =, +=, -=, *=, /=, %=  


// OPERATOR PERBANDINGAN, untuk membandingkan dua buah Operand dan Menghasilkan Nilai BOOLEAN
//    ==, !=, ===, !==, >, <, >=, <=    


// OPERATOR LOGIKA
// && (AND), || (OR), ! (NOT)
// Kalo && (AND), Dua Kondisinya Harus Bernilai TRUE, jika ingin menghasilkan nilai TRUE
// Kalo salah satunya bernilai FALSE maka hasilnya nanti akan FALSE

// Kalo || (OR), Salah satu kondisinya bernilai TRUE maka hasilnya akan TRUE
// Tetapi jika kedua kondisinya bernilai FALSE maka akan menghasilkan niai FALSE

// Kalo ! (Not), itu adalah operator pembalik keadaan


// OPERATOR STRING
// Dibuat dengan tanda + (PLUS), contohnya :
// 10 + 10 + "10" = "2010"
// "10" + 10 + 10 = "101010"


// OPERATOR TYPEOF, untuk mengetahui tipe data apa dari Nilai yang sudah kita masukan kedalamnya
// contohnya : typeof(operand)


// OPERATOR KONDISIONAL, untuk melakukan pengecekan pada suatu kondisi dan menentukan nilai yang dihasilkan 
// ketika nilainya bernilai TRUE atau FALSE
